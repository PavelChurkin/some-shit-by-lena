#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Проверка числовых утверждений к отчёту по Теории Виктора (IV) — уточнения
владельца из issue #7:

  1. Заряд — не «шарик» и не «центр сдвига», а измеримое СВОЙСТВО материи
     (физическая величина); электричеством обладают и непрерывный эфир, и
     дискретная вещественная материя.
  2. Знаки + / - — это НЕ механическое «сжатие/растяжение» эфира, а смещение
     связанного электричества под действием приложенной ЭДС: положительное по
     полю, отрицательное против поля; возникает противополе, создающее
     ВОЗВРАЩАЮЩУЮ ЭДС. Само- и взаимоиндукция — проявление ИНЕРЦИИ движения
     электричества, т.е. кинетической энергии (катушки Теслы, импульсные
     преобразователи).
  3. «Сила Лоренца в эфире» — это сила Ампера; «линий магнитной индукции»
     физически нет (это образ из железных опилок Фарадея).
  4. Эфир «вездесущий», а не «всепроникающий»; механического движения у него
     нет, но он участвует в движении вещества (эфирный увлёк / частичное
     увлечение Френеля), поэтому в замкнутом корпусе «ветра» нет.

Скрипт детерминирован, без внешних зависимостей (только стандартная библиотека
Python 3). Все «механически проверено» числа в отчёте — прямой вывод этого файла.

Запуск:
    python3 "Теория Виктора/polarization_inertia_checks.py"
"""

import math

# --- Фундаментальные константы (СИ / CODATA) ---
EPS0 = 8.8541878128e-12     # Ф/м   — электрическая постоянная (удельная ёмкость эфира)
MU0 = 1.25663706212e-6     # Гн/м  — магнитная постоянная (удельная индуктивность эфира)
C_DEF = 299792458.0        # м/с   — скорость света (точно в СИ)
E_CHARGE = 1.602176634e-19  # Кл   — элементарный заряд (квант СВОЙСТВА «электричество»)
M_E = 9.1093837015e-31     # кг    — масса электрона


def section(title):
    print("\n" + "=" * 72)
    print(title)
    print("=" * 72)


def check_charge_is_quantized_property():
    """Заряд как измеримое СВОЙСТВО (величина): он квантуется и аддитивен.

    Уточнение №1 владельца: заряд — не «шарик», а физическая величина. Самый
    прямой числовой признак «величины, а не предмета» — то, что она квантуется
    единицей e и любая наблюдаемая величина кратна e (опыт Милликена).
    """
    section("1. Заряд — измеримая ВЕЛИЧИНА (свойство), квантованная единицей e")
    print(f"  элементарный квант свойства e = {E_CHARGE:.6e} Кл")
    # Несколько «капель Милликена» как кратные e:
    for n in (1, 2, 5, 10):
        print(f"    {n} e = {n*E_CHARGE:.6e} Кл")
    print("  Любой измеренный заряд кратен e -> это ВЕЛИЧИНА (свойство материи),")
    print("  а не неделимый 'шарик'. Свойством обладают и связанные заряды среды.")


def check_polarization_restoring_emf():
    """Поляризация диэлектрика даёт ВОЗВРАЩАЮЩЕЕ противополе (уточнение №2).

    Связанное электричество смещается: '+' по полю, '-' против поля. Возникает
    поляризация P = ε0·χ·E_in и связанный заряд на гранях σ_b = P. Внутреннее
    (деполяризующее) поле направлено ПРОТИВ приложенного — это и есть
    'возвращающая ЭДС'. Никакого механического сжатия/растяжения: только сдвиг
    связанного электричества и противодействующее поле.
    """
    section("2a. Поляризация = смещение связанного электричества -> ВОЗВРАЩАЮЩЕЕ поле")
    eps_r = 4.0          # относительная диэлектрическая проницаемость (пример)
    E_applied = 1.0e5    # В/м — приложенное поле
    chi = eps_r - 1.0    # диэлектрическая восприимчивость
    # В линейном диэлектрике результирующее поле внутри = E_applied/eps_r при
    # фиксированном внешнем смещении D; противополе деполяризации:
    E_inside = E_applied / eps_r
    E_back = E_applied - E_inside       # «возвращающее» (противодействующее) поле
    P = EPS0 * chi * E_inside           # поляризация
    sigma_b = P                          # связанный поверхностный заряд, Кл/м^2
    print(f"  относительная проницаемость εr = {eps_r}, восприимчивость χ = {chi}")
    print(f"  приложенное поле  E_app   = {E_applied:.3e} В/м")
    print(f"  поле внутри       E_in    = {E_inside:.3e} В/м  (ослаблено в εr раз)")
    print(f"  возвращающее поле E_back  = {E_back:.3e} В/м  (ПРОТИВ приложенного)")
    print(f"  поляризация       P       = {P:.3e} Кл/м^2")
    print(f"  связанный заряд   σ_b = P = {sigma_b:.3e} Кл/м^2  ('+' по полю, '-' против)")
    print("  => знаки + / - — это направление сдвига связанного электричества,")
    print("     а не механическое 'сжатие/растяжение' среды.")


def check_inductive_kinetic_energy():
    """Само-/взаимоиндукция как ИНЕРЦИЯ движения электричества: W = ½·L·I².

    Уточнение №2 (вторая часть): индукция — это кинетическая энергия движущегося
    электричества. Максвелл прямо называл L·I 'электрокинетическим импульсом', а
    ½·L·I² — кинетической энергией тока. Это в точности 'инерция движения
    электричества'.
    """
    section("2b. Индукция = кинетическая энергия движущегося электричества  W=½LI²")
    L = 1.0e-3   # Гн  — индуктивность (пример: 1 мГн)
    for I in (1.0, 5.0, 10.0):
        p_kin = L * I            # 'электрокинетический импульс' Максвелла, Вб
        W = 0.5 * L * I * I      # кинетическая энергия тока, Дж
        print(f"  I={I:5.1f} А:  L·I (электрокинетич. импульс) = {p_kin:.3e} Вб,"
              f"  ½LI² = {W:.3e} Дж")
    print("  Инерция тока (само/взаимоиндукция) <-> кинетическая энергия ½LI².")


def check_tesla_lc_resonance():
    """Катушка Теслы / импульсный преобразователь: обмен ½CV² <-> ½LI².

    Энергия колеблется между электрической (½C·V², поляризация) и
    'кинетической' (½L·I², инерция движения электричества) — это и видно в
    импульсных преобразователях и резонансе катушки Теслы.
    """
    section("3. Резонанс LC (катушка Теслы): обмен ½CV² <-> ½LI²")
    L = 50e-3    # Гн
    C = 100e-12  # Ф
    f0 = 1.0 / (2 * math.pi * math.sqrt(L * C))
    # При полном переходе энергии: ½CV² = ½LI²  ->  I_peak = V·sqrt(C/L)
    V = 1.0e4
    W_e = 0.5 * C * V * V
    I_peak = V * math.sqrt(C / L)
    W_m = 0.5 * L * I_peak * I_peak
    print(f"  L={L*1e3:.0f} мГн, C={C*1e12:.0f} пФ -> f0 = {f0/1e3:.2f} кГц")
    print(f"  при V={V:.0e} В:  ½CV² = {W_e:.3e} Дж  =  ½LI² = {W_m:.3e} Дж")
    print(f"  пик тока I = V·sqrt(C/L) = {I_peak:.3e} А  (энергия целиком 'кинетическая')")


def check_lorentz_is_ampere():
    """'Сила Лоренца' = сила Ампера; магнитная часть — это (v/c)²-поправка к электрике.

    Уточнение №3: сила на движущийся заряд — это сила Ампера между токами;
    'линий магнитной индукции' как физического объекта нет. Численно: магнитная
    сила между двумя зарядами, движущимися со скоростью v, относится к
    кулоновской как (v/c)². Для двух токов это сворачивается в формулу Ампера
    F/L = μ0·I1·I2/(2π·d), вообще не требуя 'линий поля'.
    """
    section("4. 'Сила Лоренца' = сила Ампера (магнетизм = (v/c)²-поправка к электрике)")
    for v in (1.0e3, 1.0e6, 2.0e7):
        ratio = (v / C_DEF) ** 2
        print(f"  v={v:8.1e} м/с:  F_магн/F_кулон = (v/c)² = {ratio:.3e}")
    print("  -> магнитная сила — относительная поправка к электрической при движении.")
    # Та же физика без 'линий': сила Ампера между двумя параллельными токами.
    I1 = I2 = 1.0   # А
    d = 1.0         # м
    F_per_L = MU0 * I1 * I2 / (2 * math.pi * d)
    print(f"  Сила Ампера между токами 1 А на расстоянии 1 м: F/L = {F_per_L:.3e} Н/м")
    print("  (ровно 2e-7 Н/м — старое определение ампера; 'линии поля' не нужны).")


def check_ether_drag_aberration_fizeau():
    """Эфир 'участвует в движении вещества': частичное увлечение Френеля.

    Уточнение №4: внутри замкнутого корпуса 'ветра' нет, снаружи — есть. Это
    язык увлечения эфира. Контрольные числа мейнстрима, с которыми такая
    картина обязана считаться:
      * звёздная аберрация: угол α = v/c (полное увлечение её бы убрало);
      * опыт Физо: свет в текущей воде увлекается лишь ЧАСТИЧНО, с коэффициентом
        Френеля f = 1 − 1/n² (не полностью!).
    Числа честно показывают цену гипотезы полного увлечения.
    """
    section("5. 'Участие эфира в движении вещества': аберрация и увлечение Физо")
    v_orb = 2.98e4   # м/с — орбитальная скорость Земли
    alpha = v_orb / C_DEF
    print(f"  Звёздная аберрация: α = v/c = {alpha:.3e} рад = {math.degrees(alpha)*3600:.2f}\"")
    print("    (наблюдается ~20.5\"; ПОЛНОЕ увлечение эфира её обнулило бы — но она есть)")
    n_water = 1.333
    fresnel = 1.0 - 1.0 / n_water ** 2
    print(f"  Коэффициент увлечения Френеля для воды (n={n_water}): f = 1 − 1/n² = {fresnel:.3f}")
    print("    (опыт Физо: увлечение ЧАСТИЧНОЕ, не полное -> простой 'эфирный пассажир'")
    print("     внутри тел количественно не выходит без поправки Френеля/СТО).")
    # Майкельсон–Морли: ожидаемый сдвиг полос против наблюдённого нуля.
    L_arm = 11.0       # м — эффективная длина плеча (1887)
    lam = 5.9e-7       # м — жёлтый свет
    shift = 2.0 * L_arm * (v_orb / C_DEF) ** 2 / lam  # фактор 2 — поворот на 90°
    print(f"  Майкельсон–Морли (плечо {L_arm:.0f} м): ожидался сдвиг ~{shift:.2f} полосы,")
    print("    наблюдался ~0 -> внутри замкнутой лаборатории 'ветра' нет (увлечение/инвариантность).")


def main():
    print("Проверки к Теории Виктора (IV): заряд-свойство, поляризация и возвращающая")
    print("ЭДС, индукция как кинетическая энергия, Лоренц=Ампер, увлечение эфира.")
    check_charge_is_quantized_property()
    check_polarization_restoring_emf()
    check_inductive_kinetic_energy()
    check_tesla_lc_resonance()
    check_lorentz_is_ampere()
    check_ether_drag_aberration_fizeau()
    print("\nГотово. Все числа выше воспроизводимы запуском этого скрипта.")


if __name__ == "__main__":
    main()
